#Variable Assignment and Arithmetic Operations:


name = "Mustafa"  # variable assignment
print(name)       # printing the variable

length = 5
area = length * length  # arithmetic operation
print(area)

x = 5
y = 10
print(x, y)

x = y
print(x, y)

x = 5
print(x)

x = x + 3  # arithmetic operation
print(x)

x = 5
x  # printing the variable
